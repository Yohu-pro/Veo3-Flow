export function translate(key: keyof typeof TRANSLATIONS.VN, lang: 'EN' | 'VN', params?: Record<string, any>) {
  let text = TRANSLATIONS[lang][key] || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  return text;
}
